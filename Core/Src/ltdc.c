void LCD_Fill(uint16_t x,uint16_t y,uint16_t width,uint16_t high,uint32_t color){
		uint8_t *start_adr = (uint8_t *)(SDRAM_BANK1_START_ADRESS + x*2 + y*800*2) ;
	  uint32_t total = width * high * 2;
		uint32_t j;
		for(j=0;j<total;j++){
			*start_adr = color;
			start_adr++;
		}
}




